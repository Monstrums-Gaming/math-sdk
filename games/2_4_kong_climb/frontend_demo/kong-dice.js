// kong-dice.js
// -----------------------------------------------------------------------------
// Standalone, zero-build frontend for Kong Climb (2_4_kong_climb) — a Stake-style
// dice game. It replays the REAL generated math (the published lookup tables),
// not a client-side formula.
//
// Canonical dice modes: over_NN / under_NN, where NN is the integer slider target.
//   under_NN wins if the roll < NN   -> winChance = NN%
//   over_NN  wins if the roll > NN   -> winChance = (100 - NN)%
// over and under are NOT mirrors: at the same target they have different win
// chances (except NN=50).
//
// Runs in two modes, auto-detected from the launch URL (same pattern as
// games/3_2_mystery_box_cash_paradise/frontend_demo/app.js):
//   LIVE RGS  -> rgs_url + sessionID present: /wallet/authenticate -> /wallet/play
//                -> /wallet/end-round.
//   LOCAL     -> otherwise: weighted-picks an outcome from the mode's lookup table
//                (kong_dice_rgs.json, generated by build_demo_data.py) and builds
//                the SAME round shape the RGS returns. Fully offline.
//
// Every Kong lookup table has uniform weight 1 and exactly two payouts (0 or the
// win value), so a weighted pick + synthesized diceResult/finalWin events
// reproduces the real book exactly.
// -----------------------------------------------------------------------------

const API_MULTIPLIER = 1_000_000; // RGS money: integer, 6 decimals (1_000_000 == 1.0)
const EVENT_SCALE = 100; // payoutMultiplier / event amounts are base-bet x100 (194 == 1.94x)
const LOCAL_START_BALANCE = 1000;
const DATA_URL = "./kong_dice_rgs.json";

// Shared dice-value -> tower-height mapping (% of the game-stage). Used by the
// ape, the right-side marks, the winning-zone shading and the roll animation, so
// everything lives on one 0-100 scale. v=0 -> 8%, v=100 -> 80%.
const AXIS_BOTTOM = 8;
const AXIS_SPAN = 72;
const clampV = (v) => Math.max(0, Math.min(100, v));
const heightPct = (v) => AXIS_BOTTOM + (clampV(v) / 100) * AXIS_SPAN;

